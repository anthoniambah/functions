// A simple loop, inside a function, that takes an array and prints the value of the array to the console.

function printArray(perfume) {
  perfume = ["Ophylia", "Alessa", "Valencia", "Lataffa", "Smart", "Oud"];
  for (let i = 0; i < perfume.length; i++) {
    console.log(perfume[i]);
  }
}
printArray();
